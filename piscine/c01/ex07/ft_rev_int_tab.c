void	ft_rev_int_tab(int *tab, int size)
{
	int	j;
	int	tmp;

	j = 0;
	while (j < (size / 2))
	{
		tmp = tab[j];
		tab[j] = tab[size - j - 1];
		tab[size - j - 1] = tmp;
		j++;
	}
}
